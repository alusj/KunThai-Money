const LOCALE_BY_LANGUAGE = {
  en: "en-US",
  fr: "fr-FR",
  ar: "ar"
};

function interpolate(template, values) {
  return template.replace(/\{(\w+)\}/g, (_, key) => `${values?.[key] ?? `{${key}}`}`);
}

export function useTranslation() {
  const { resolvedLanguage } = useAppearance();
  const language = resolvedLanguage === "fr" || resolvedLanguage === "ar" ? resolvedLanguage : "en";

  const t = (key, values) => {
    const translated = TRANSLATIONS[language]?.[key] || key;
    return values ? interpolate(translated, values) : translated;
  };

  return {
    language,
    locale: LOCALE_BY_LANGUAGE[language] || LOCALE_BY_LANGUAGE.en,
    isRTL: language === "ar",
    t,
  };
}
